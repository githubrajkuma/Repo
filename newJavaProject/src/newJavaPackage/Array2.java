package newJavaPackage;

public class Array2 {

	public static void main(String[] args) {

		//defining String an array
		String a[] = {"Gopal" ,"Ram" ,"Hare Krishna"};
		
		//defining an int array
		int b[]= {1,2,3,4,5,6};
		
		//defining an Object array
		Object c[]= {12,23,13,24,"Arjun","Bhim" ,2.3,3.2,1.3};
		
		
		System.out.println(a[1]);//Ram
		System.out.println(b[5]);//6
		System.out.println(c[4]);//Arjun
		
	}

}
